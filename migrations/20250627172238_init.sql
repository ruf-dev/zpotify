       ('ru')
ON CONFLICT DO NOTHING;

CREATE TABLE IF NOT EXISTS user_settings
(
    user_id INT8 REFERENCES users (id) UNIQUE NOT NULL,
    locale  TEXT REFERENCES locales (id)      NOT NULL DEFAULT 'en'
);

CREATE TABLE IF NOT EXISTS user_permissions
(
    user_id             INT8 REFERENCES users (id) NOT NULL,
    can_upload          BOOL    DEFAULT false      NOT NULL,
    early_access        BOOL    DEFAULT false      NOT NULL,
    can_create_playlist BOOLEAN DEFAULT false      NOT NULL
);

CREATE TABLE IF NOT EXISTS files_meta
(
    id           INT8 NOT NULL PRIMARY KEY GENERATED ALWAYS AS identity,
    file_path    TEXT NOT NULL,
    duration_sec INT8 NOT NULL,
    added_by_id  INT8 NOT NULL REFERENCES users (id),
    size_bytes   INT8 NOT NULL
);

CREATE TABLE IF NOT EXISTS user_sessions
(
    user_id           INT8 REFERENCES users (id) NOT NULL,

    access_token      TEXT PRIMARY KEY           NOT NULL,
    refresh_token     TEXT UNIQUE                NOT NULL,
    access_expire_at  TIMESTAMP                  NOT NULL,
    refresh_expire_at TIMESTAMP                  NOT NULL
);

CREATE TABLE IF NOT EXISTS artists
(
    uuid UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    name TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS songs
(
    id         INT8 PRIMARY KEY GENERATED ALWAYS AS IDENTITY,
    file_id    INT8 REFERENCES files_meta (id) NOT NULL,
    title      TEXT                            NOT NULL,
    created_at TIMESTAMP DEFAULT now()         NOT NULL
);

CREATE TABLE IF NOT EXISTS songs_artists
(
    song_id     INT8 REFERENCES songs (id)     NOT NULL,
    artist_uuid UUID REFERENCES artists (uuid) NOT NULL,
    order_id    INT8                           NOT NULL,
    UNIQUE (song_id, order_id),
    UNIQUE (song_id, artist_uuid)
);


